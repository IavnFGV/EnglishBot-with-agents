def normalize_language_code(language_code: str | None) -> str:
    if language_code in SUPPORTED_LANGUAGE_CODES:
        return str(language_code)
    return DEFAULT_LANGUAGE_CODE


def get_language_label(language_code: str) -> str:
    normalized = normalize_language_code(language_code)
    return LANGUAGE_LABELS[normalized]


def translate(
    key: str,
    language_code: str | None = None,
    **params: object,
) -> str:
    normalized_language_code = normalize_language_code(language_code)
    template = TRANSLATIONS.get(normalized_language_code, {}).get(key)
    if template is None:
        template = TRANSLATIONS[DEFAULT_LANGUAGE_CODE].get(key)
    if template is None:
        raise KeyError(f"Missing translation key: {key}")
    return template.format(**params)


def translate_for_user(
    user_id: int,
    key: str,
    **params: object,
) -> str:
    from .user_profiles import get_user_language

    return translate(key, get_user_language(user_id), **params)
